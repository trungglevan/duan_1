            <h1> 2.MÌ QUẢNG</h1>
            <img src="https://baosongngu.vn/wp-content/uploads/2020/07/mon-an-viet-nam-bang-tieng-anh.jpg" alt=""
                width="420" height="363">
            <p>

                Không còn nghi ngờ gì nữa, món ăn bị đánh giá thấp hơn thực tế nhất tại Việt Nam chính là Mì
                Quảng.<br>
                <br>
                Mì quảng có thể xuất hiện ở hầu hết các quán ăn tại Việt Nam, nhưng món ăn này thực tế xuất xứ
                từ Đà Nẵng.<br>
                <br>
                Được phân biệt rõ ràng nhờ sợi mỳ gạo màu vàng, món ăn này là sự kết hợp xiêu lòng từ nước dùng
                từ xương được nêm nếm bởi nước mắm, tiêu đen, củ hẹ, và tỏi, cùng với các nguyên liệu từ thịt
                khác như tôm sông, trứng cút luộc, và thịt lợn nướng.<br>
                <br>
                Như hầu hết các món ăn Việt Nam, Mì Quảng cũng ăn cùng với nhiều loại rau sống khác nhau, bao
                gồm húng quế, lạc, rau mùi, xà lách, hoa chuối thái mỏng, và bánh đa mè. <br>
                <br>
                Không có công thức cố định nào cho Mì Quảng - nó tùy thuộc vào khẩu vị và sở thích của từng
                người. <br>
                <br>
                Với hầu hết dân Đà Nẵng, Mì Quảng ngon nhất là được làm tại nhà bởi mẹ hoặc bà của họ, cùng với
                tình yêu và những kỷ niệm. <br>
                <br>
                Mì Quảng đã đem đến cảm giác chân thực và giản dị như con người xứ miền Trung Việt Nam, điều mà
                khó có thể thấy ở những món ăn hiện đại.<br>
            </p>
            <h1> 3.BÁNH XÈO</h1>
