                width="420" height="363">
            <p>

                (Bánh xèo ở Việt Nam có nhiều nguồn gốc khác nhau. Bánh xèo miền Trung có nguồn gốc từ Bình
                Định, bánh xèo miền Tây lại có nguồn gốc từ người Khmer.
                Mặc dù có nhiều nguồn gốc, nhưng cách thức chế biến món bánh xèo đều giống nhau, hương vị nêm
                nếm có thể khác nhau từng vùng miền.)<br>
                <br>
                (Đây là món ăn đậm nét truyền thống của người Việt Nam. Đặc biệt là vào những ngày mưa, được ăn
                món bánh xèo vừa nóng hổi, vừa giòn thì không còn điều gì tuyệt vời hơn.)<br>
                <br>
                (Nguyên liệu chính làm món bánh xèo chính là : bột, tôm, thịt, giá đổ, hành lá cắt nhỏ. Ăn kèm
                với món bánh xèo sẽ là nước chấm cay ngọt, cùng với rổ rau sống.)<br>
                <br>
                (Có thể nói, đây là món ăn được hầu hết mọi người yêu thích. Bởi vì Bánh Xèo khá ngon và dễ
                ăn.)<br>

            </p>
            <h1> 4.BÁNH CUỐN</h1>
            <img src="https://kenh14cdn.com/2018/4/13/bc8-15236160240172078798368.jpg" alt="" width="420" height="363">
            <p>

                Bánh cuốn là món ăn tuyệt với khi bạn đang cảm thấy lúng túng trong khi ngắm cảnh tại Việt Nam.
                <br>
                <br>
                Sự kết hợp của thịt xay, mộc nhĩ thái nhỏ, hành, chả lụa, được gói trong một tấm bột gạo hấp,
                hương vị tổng thể của món ăn này trung hòa một cách đáng ngạc nhiên dù các nguyên liệu có đậm
                vị. <br>
                <br>
                Để tăng thêm hương vị, bạn có thể nhúng bánh cuốn vào nước chấm mắm. Bởi sự nổi tiếng với các du
                khách, bạn có thể tìm thấy rất nhiều hàng quán ven đường bán bánh cuốn gần các điểm du lịch và
                các nơi sống về đêm.<br>
                <br>
                Ngày nay, nó trở thành một món ăn phổ thông có thể ăn bất cứ thời điểm nào trong ngày. Đó có thể
                là món ăn trưa nhẹ nhanh chóng để kịp giờ làm văn phòng hoặc là bữa tối nhỏ cho khoảng thời gian
                trò chuyện với bạn bè.<br>
                <br>
                Trong gia đình, bánh cuốn là một món ăn hoàn hảo để tụ tập các thành viên trong gia đình, từ
                khâu chuẩn bị đến cuốn bánh, và chia sẻ niềm vui với gia đình. Kể cả khi bánh cuốn được hấp theo
                cách truyền thống hay bằng chảo chống dính hiện đại, <br>
                <br>
                một chiếc bánh cuốn đượm vị dù nhỏ hay to, chứa hoặc không chứa nhiều loại nhân, bánh cuốn là
                một món ăn được yêu thích lâu dài tại Việt Nam và của nhiều du khách. Bánh cuốn là một món cuốn
                chứa đựng tất cả văn hóa và tinh thần ẩm thực của nhiều vùng khác nhau trên đất nước.<br>

            </p>



